function applyDark(on) {
    document.documentElement.classList.toggle('dark', on);
    const icon = on ? 'light_mode' : 'dark_mode';
    const label = on ? 'Light Mode' : 'Dark Mode';
    const ti = document.getElementById('topDarkIcon');
    const si = document.getElementById('sbDarkIcon');
    const sl = document.getElementById('sbDarkLabel');
    const li = document.getElementById('dIcon'); // legacy id used in some pages
    if(ti) ti.textContent = icon;
    if(si) si.textContent = icon;
    if(sl) sl.textContent = label;
    if(li) li.textContent = icon;
}
function toggleDarkMode() {
    const on = !document.documentElement.classList.contains('dark');
    localStorage.setItem('cc_dark', on ? '1' : '0');
