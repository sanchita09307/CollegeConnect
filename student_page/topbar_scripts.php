function toggleDark() {
    document.documentElement.classList.toggle('dark');
    const icon = document.getElementById('darkIcon');
    if (icon) icon.textContent = document.documentElement.classList.contains('dark') ? 'light_mode' : 'dark_mode';
    localStorage.setItem('cc_dark', document.documentElement.classList.contains('dark') ? '1' : '0');
}
(function(){
    if (localStorage.getItem('cc_dark') === '1') {
        document.documentElement.classList.add('dark');
        document.addEventListener('DOMContentLoaded', () => {
            const icon = document.getElementById('darkIcon');
            if (icon) icon.textContent = 'light_mode';
        });
    }
})();

